package Day2;

public class C07_AritmetikAtamaOperatorleri {

    public static void main(String[] args) {
        int sayi =20;
        sayi=sayi*5;

        System.out.println(sayi);//100
        sayi=sayi+5;
        System.out.println(sayi);//105
        sayi=sayi-5;
        System.out.println(sayi);//100

        sayi=sayi/5;
        System.out.println(sayi);//20

        sayi=sayi%5;
        System.out.println(sayi);//0



    }
}
