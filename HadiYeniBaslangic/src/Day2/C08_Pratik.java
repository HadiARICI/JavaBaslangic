package Day2;

public class C08_Pratik {
    public static void main(String[] args) {

        //25 ve 5 sayilarini kullanarak 4 islem yapiniz  4 islem uygularken atama oparatorlerini kullanınız

        int sayi1=26;
        int sayi2=5;

        sayi1+=sayi2;
        System.out.println(sayi1);

        sayi1*=sayi2;
        System.out.println(sayi1);

        sayi1/=sayi2;
        System.out.println(sayi1);

        sayi1%=sayi2;
        System.out.println(sayi1);



    }

}
