package Day9;

public class C04_Pratik {
    public static void main(String[] args) {
        /*
        while döngüsünü kullanarak 1 den 50 kadar tüm sayıları console yazdırın

         */
        int sayi=1;

        while (sayi<=50){

            System.out.print(sayi+",");

            sayi++;
        }
        System.out.println("alt satır çalışsın");

    }
}
